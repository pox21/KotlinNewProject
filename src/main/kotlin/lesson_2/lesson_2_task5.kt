package lesson_2

fun main() {
//    Напиши программу-калькулятор для банковского приложения
//    (такой функционал бывает на промо экранах).
//    Используя формулу сложных процентов (ее можно загуглить),
//    нужно посчитать размер вклада через 20 лет с такими условиями:
//
//    – Клиент ввел в приложении сумму 70_000 рублей
//    (считать ее известной заранее и проинициализировать переменную);
//    – Процентная ставка 16,7% (проценты начисляются в конце года);
//    – Подсчитать размер вклада через 20 лет;
//
//    – Результат должен быть дробным числом с 3 знаками после разделителя;
//    – Ответ должен получиться таким 1536438.428.

    // Входные данные
    val initialDeposit: Double = 70_000.0
    val annualInterestRate: Double = 0.167   // 16.7% = 0.167
    val years: Int = 20

    // Считаем множитель (1 + r)^n через цикл
    var multiplier: Double = 1.0
    for (i in 1..years) {
        multiplier *= (1 + annualInterestRate)
    }

    // Формула сложных процентов: A = P * (1 + r)^n
    val finalAmount: Double = initialDeposit * multiplier

    // Вывод с 3 знаками после разделителя
    println(String.format("%.3f", finalAmount))

}